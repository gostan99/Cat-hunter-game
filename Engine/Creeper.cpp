#include "Creeper.h"
#include <assert.h>

void Creeper::Init(Vector2 in_loc, Vector2 in_velocity)
{
	assert(initialized == false);
	loc = in_loc;
	velovity = in_velocity;
	initialized = true;
}

void Creeper::update( float dt)
{
	assert(initialized == true);
	loc += velovity * dt;

	const float right = loc.x + Width;
	if (loc.x < 0)
	{
		loc.x = 0;
		velovity.x = -velovity.x;
	}
	else if (right >= float(Graphics::ScreenWidth))
	{
		loc.x = float(Graphics::ScreenWidth - 1) - Width;
		velovity.x = -velovity.x;
	}

	const float bottom = loc.y + Height;
	if (loc.y < 0)
	{
		loc.y = 0;
		velovity.y = -velovity.y;
	}
	else if (bottom >= float(Graphics::ScreenHeight))
	{
		loc.y = float(Graphics::ScreenHeight - 1) - Height;
		velovity.y = -velovity.y;
	}
}

float Creeper::getWidth() const
{
	return Width;
}

float Creeper::getHeight() const
{
	return Height;
}

Vector2 Creeper::getLoc() const
{
	return loc;
}

